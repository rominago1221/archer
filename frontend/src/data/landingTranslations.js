// Map (jurisdiction, language) -> locale key for landing page translations
export const getLocaleFromPrefs = (jurisdiction, language) => {
  const j = jurisdiction || 'US';
  const l = (language || 'en').replace(/-.*/, '');
  // Try exact match first
  const exact = `${j.toLowerCase()}-${l}`;
  if (translations[exact]) return exact;
  // Fallback: use jurisdiction default
  if (j === 'BE') return 'be-fr';
  return 'us-en';
};

export const getStoredLocale = () => {
  try {
    const stored = localStorage.getItem('jasper_locale');
    if (stored && translations[stored]) return stored;
  } catch (e) {}
  return 'us-en';
};

export const setStoredLocale = (key) => {
  try { localStorage.setItem('jasper_locale', key); } catch (e) {}
};

